    private void btn_cuota_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cuota_ActionPerformed
            if (rbn_6meses_.isSelected()==true){
                interes=precio/6;
            }
            if (rbn_12meses_.isSelected()==true){
                interes=precio/12;
            }
            if (rbn_24meses_.isSelected()==true){
                interes=precio/24;
            }
            JOptionPane.showMessageDialog(null, "Gracias por usar el programa");

    }//GEN-LAST:event_btn_cuota_ActionPerformed

