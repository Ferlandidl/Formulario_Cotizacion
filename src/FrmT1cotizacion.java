    private void cbx_articulos_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbx_articulos_ActionPerformed
        int posicion=0; 
        posicion=cbx_articulos_.getSelectedIndex();
        switch (posicion) {
            case 1:
                precio=1800;
                muestra = "3%";
                compra="Televisor";
                break;
            case 2:
                precio=1750;
                muestra = "5%";
                compra="Heladera";
                break;
            case 3:
                precio=2000;
                muestra = "8%";
                compra="Cocina";
                break;
            case 4:
                precio=2600;
                muestra = "10%";
                compra="Computadora";
                break;
        }
        txt_precio_.setText(String.valueOf(precio));
        txt_interes_.setText(String.valueOf(muestra));
    }//GEN-LAST:event_cbx_articulos_ActionPerformed

